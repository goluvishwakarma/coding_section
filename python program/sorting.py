# sorting is arranging the data in either ascending or descending order

str_lst = ['2', '0', '1', '-1', '4', '3', '1.2', '5', '9']
str_lst.sort()
print(str_lst)


num_lst = [2, 34, -3, 0, 45, 2.1, 3, 9, 4, 5, 7, -1, 1, 6]
num_lst.sort()
print(num_lst)

vowel = ['i', 'u', 'a', 'o', 'e']
vowel.sort(reverse=True)  # descending order
print(vowel)
print(vowel[::-1])


