# ----------------- Operators in python ----------------

"""
    *. following are some common operator in python:
    1. Arithmetic operator ---- +, -, *, %, /
    2. Assignment operator ---- =, +=, -=, *= etc.
    3. Comparison operator ---- ==, !=, >, <, =>, <=
    4. Logical operator ----    and, or and not
"""
a = 7
b = 3

# Arithmetic operator:
print("the value of a snd b is: ", (a + b))
print("the value of a snd b is: ", (a - b))
print("the value of a snd b is: ", (a * b))
print("the value of a snd b is: ", (a % b))
print("the value of a snd b is: ", (a / b))

# Assignment operator:
a += 6
print("new value of a is: ", a)

# Comparison operator:
print(4 == 9)

# Logical operator:
bool1 = True
bool2 = False
print("the value of bool1 nad bool2 is: ", bool1 and bool2)
print("the value of bool1 nad bool2 is: ", bool1 or bool2)
print("the value of bool1 nad bool2 is: ", not bool2)
