strA = input("Enter a positive integer: ")
strB = input("Enter another integer: ")

intA = int(strA)
intB = int(strB)

# basic calculation,
print(intA, " +(Addition) ", intB, " = ", (intA + intB))
print(intA, " -(Subtraction) ", intB, " = ", (intA - intB))
print(intA, " *(Multiplication) ", intB, " = ", (intA * intB))
print(intA, " %(Modulo) ", intB, " = ", (intA % intB))
print(intA, " /(Division) ", intB, " = ", (intA / intB))
print(intA, " //(Floor_Division) ", intB, " = ", (intA // intB))
